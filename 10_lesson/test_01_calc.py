# импортируем allure для описания теста и шагов отчета
import allure

# импортируем webdriver для запуска браузера
from selenium import webdriver

# импортируем page object страницы калькулятора
from pages import CalcPage


# название теста в allure-отчете
@allure.title("Проверка результата медленного калькулятора")
# описание теста в allure-отчете
@allure.description(
    "Тест открывает калькулятор, задает задержку, выполняет сложение "
    "7 + 8 и проверяет, что на экране появился результат 15."
)
# функциональность, к которой относится тест
@allure.feature("Медленный калькулятор")
# критичность теста в отчете
@allure.severity(allure.severity_level.CRITICAL)
def test_slow_calculator() -> None:
    """проверяет сложение чисел в медленном калькуляторе"""
    # запускаем браузер chrome
    driver = webdriver.Chrome()

    try:
        # создаем объект страницы калькулятора
        calc = CalcPage(driver)

        # фиксируем шаг открытия страницы в allure-отчете
        with allure.step("Открыть страницу медленного калькулятора"):
            # открываем страницу калькулятора
            calc.open()

        # фиксируем шаг настройки задержки в allure-отчете
        with allure.step("Установить задержку вычисления 45 секунд"):
            # устанавливаем задержку вычисления 45 секунд
            calc.set_delay("45")

        # фиксируем шаг ввода выражения в allure-отчете
        with allure.step("Нажать кнопки выражения 7 + 8 ="):
            # нажимаем кнопку 7
            calc.click_button("7")

            # нажимаем кнопку сложения
            calc.click_button("+")

            # нажимаем кнопку 8
            calc.click_button("8")

            # нажимаем кнопку равно
            calc.click_button("=")

        # фиксируем шаг ожидания результата в allure-отчете
        with allure.step("Дождаться результата вычисления"):
            # ждем результат 15 не больше 50 секунд
            result = calc.get_result(50, "15")

        # фиксируем шаг проверки результата в allure-отчете
        with allure.step("Проверить, что результат равен 15"):
            # проверяем, что фактический результат равен ожидаемому
            assert result == "15"
    finally:
        # закрываем браузер после теста
        driver.quit()
